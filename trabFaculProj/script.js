// Função para parar o vídeo
function stopVideo(videoFrame) {
    videoFrame.src = videoFrame.src; // Isso irá recarregar o iframe e parar o vídeo
  }
  
  // Abrir o modal e mostrar o vídeo
  document.getElementById('openModal1').addEventListener('click', function () {
    var modal = document.getElementById('myModal1');
    var video = document.getElementById('video1');
    modal.style.display = 'block';
    video.style.display = 'block'; // Certifique-se de que o vídeo seja exibido
  });
  
  // Fechar o modal e parar o vídeo
  document.getElementById('fechar1').addEventListener('click', function () {
    var modal = document.getElementById('myModal1');
    var video = document.getElementById('video1');
    modal.style.display = 'none';
    stopVideo(video); // Para o vídeo
  });
  
  // Fechar o modal e parar o vídeo quando clicar fora
  window.addEventListener('click', function (event) {
    var modal = document.getElementById('myModal1');
    var video = document.getElementById('video1');
    if (event.target == modal) {
      modal.style.display = 'none';
      stopVideo(video); // Para o vídeo
    }
  });
